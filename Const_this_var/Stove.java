class Stove {
	
	String name = "SSSSSSSSSSSSSSS";
	String flame;
	int price;
	String capacity;
	int weight;
	int width;
	int height;
	String type;

	Stove(String name,String flame,int price,String capacity,int weight,int width,int height,String type)
	{
		this.name = name;
		this.flame = flame;
		this.price = price;
		this.capacity = capacity;
		this.weight = weight;
		this.width = width;
		this.height = height;
		this.type = type;

	}
}

